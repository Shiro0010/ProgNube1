angulo = float(input("Ingrese el ángulo en grados: "))

if angulo < 90:
    tipo_angulo = "Agudo"
elif angulo > 90:
    tipo_angulo = "Obtuso"
else:
    tipo_angulo = "Recto"

print("El ángulo es:", tipo_angulo)

