'''Crearemos un programa que nos permita calcular el area de un triángulo, el usuario debe insertar la información desde el teclado'''
#Vamos a definir las variables
base = (float(input("Inserte la base del triangulo: ")));
altura= (float(input("Inserte la Altura De el Triángulo: ")));
#aquí se dará el proceso de calcular el area del triángulo
area = (base * altura)/2;
#a continuación se muestra la información en pantalla
print("el area del triangulo es igual a: " + str(area)); 
	

